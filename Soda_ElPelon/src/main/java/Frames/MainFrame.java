    private void darkModeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_darkModeMouseClicked
    /*Compara el tema en el que esté el programa, si esta en tema claro se cambia de icono a la luna, el color a oscuro y viceversa*/
        if (content.getBackground().equals(new java.awt.Color(51, 51, 51))) {
            content.setBackground(new java.awt.Color(255, 255, 255));
            ImageIcon dark = new ImageIcon("C:\\Users\\admin\\OneDrive\\Documentos\\Soda\\Soda_ElPelon\\src\\main\\resources\\darkMode.png");            
            darkMode.setIcon(dark);            
        } else if (content.getBackground().equals(new java.awt.Color(255, 255, 255))) {
            content.setBackground(new java.awt.Color(51, 51, 51));
            ImageIcon light = new ImageIcon("C:\\Users\\admin\\OneDrive\\Documentos\\Soda\\Soda_ElPelon\\src\\main\\resources\\ligthMode.png");
            darkMode.setIcon(light);
        }                             
        
        if (softDrinks.getBackground().equals(new java.awt.Color(51, 51, 51))) {
            softDrinks.setBackground(new java.awt.Color(255, 255, 255));
            ImageIcon dark = new ImageIcon("C:\\Users\\admin\\OneDrive\\Documentos\\Soda\\Soda_ElPelon\\src\\main\\resources\\darkMode.png");
            darkMode.setIcon(dark);
            titleSd.setForeground(Color.black);
        } else if (softDrinks.getBackground().equals(new java.awt.Color(255, 255, 255))) {
            softDrinks.setBackground(new java.awt.Color(51, 51, 51));
            ImageIcon light = new ImageIcon("C:\\Users\\admin\\OneDrive\\Documentos\\Soda\\Soda_ElPelon\\src\\main\\resources\\ligthMode.png");
            darkMode.setIcon(light);
            titleSd.setForeground(Color.white);
        } 
        
    }//GEN-LAST:event_darkModeMouseClicked

